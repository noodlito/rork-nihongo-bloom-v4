function shuffleOptions(question: TriviaQuestion): TriviaQuestion {
  const indices = question.options.map((_, i) => i);
  for (let i = indices.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [indices[i], indices[j]] = [indices[j], indices[i]];
  }
  const newOptions = indices.map(i => question.options[i]);
  const newOptionsJp = indices.map(i => question.optionsJp[i]);
  const newCorrectIndex = indices.indexOf(question.correctIndex);
  return {
    ...question,
    options: newOptions,
    optionsJp: newOptionsJp,
    correctIndex: newCorrectIndex,
  };
}

